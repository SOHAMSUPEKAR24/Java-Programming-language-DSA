public class Switch{
    public static void main(String args[]){
        int a = 2;
        switch(a){
            case 1 : System.out.print("me");
            break;
            case 2 : System.out.print("you");
            break;
            case 3 : System.out.print("they");
            break;
            default : System.out.print("everyone");
        }
    }
}